import type { LanguageModel, ModelMessage, ToolSet } from 'ai';
import type { McpServerConfig } from './mcp.js';

// Progress events emitted during agent execution
export interface ProgressEvent {
  type:
    | 'thinking'
    | 'tool-call'
    | 'tool-result'
    | 'text'
    | 'step-complete'
    | 'done'
    | 'error'
    | 'debug';
  content: string;
  toolName?: string;
  toolArgs?: unknown;
  /**
   * Structured debug payload for `type === 'debug'` events. See
   * {@link DebugEvent} for the per-channel shape. Only populated when
   * `AgentConfig.debug` is set. Progress consumers that don't care
   * about debug can filter `type === 'debug'` out.
   */
  debug?: DebugEvent;
  /**
   * Full raw tool result. Only populated on `tool-result` events when the
   * agent was created with `emitFullResult: true`. See issue #48.
   *
   * Default behaviour emits only `summary` + `data` so secrets / large
   * file contents don't leak through logs and telemetry.
   */
  toolResult?: unknown;
  /**
   * Short human-readable summary of a `tool-result` event. Includes paths,
   * sizes, error codes — safe for operator logs; not intended for the model.
   */
  summary?: string;
  /**
   * Structured metadata about the tool invocation (tool-specific keys like
   * `path`, `bytes`, `matchCount`). Stable enough for programmatic consumers.
   */
  data?: Record<string, unknown>;
  /** True when a `tool-result` represents a blocked / denied operation. */
  blocked?: boolean;
  step?: number;
  totalSteps?: number;
}

// ─── Debug / observability (#72) ────────────────────────────────────────

/**
 * Per-channel debug event. See {@link DebugConfig} for how to opt in.
 *
 * The event surface is deliberately narrow and non-abstract — each
 * channel matches one thing you'd want to grep for when diagnosing
 * prompt / caching / tool-call problems. If you need richer tracing,
 * wire {@link AgentConfig.debug.sink} to an OpenTelemetry span emitter
 * and keep the progress stream unchanged.
 */
export type DebugEvent =
  /** The resolved system prompt, emitted once per run before any model call. */
  | {
      channel: 'system-prompt';
      step: number;
      content: string;
      bytes: number;
      /** True when the body was clipped to `maxBodyBytes`. */
      truncated: boolean;
    }
  /** The full message list going into a single `streamText` call. */
  | {
      channel: 'messages';
      step: number;
      messages: ModelMessage[];
      bytes: number;
      truncated: boolean;
    }
  /** Metadata about the request about to go to the provider. */
  | {
      channel: 'request';
      step: number;
      model: string;
      toolCount: number;
      toolNames: string[];
      providerOptions?: Record<string, unknown>;
    }
  /** A single raw stream part coming back from the provider. */
  | {
      channel: 'response-part';
      step: number;
      partType: string;
      /**
       * The raw stream part verbatim (text-delta, tool-call, reasoning-delta,
       * finish, etc.). Only populated in `trace` level — at `debug` level
       * we emit the partType counter only to keep logs bounded.
       */
      part?: Record<string, unknown>;
    }
  /** Per-step token accounting, including cache breakdown. */
  | {
      channel: 'cache';
      step: number;
      /** Input tokens that hit the prompt cache (cheap). */
      cacheReadTokens: number;
      /** Input tokens that wrote a new cache entry (premium). */
      cacheWriteTokens: number;
      /** Input tokens not served from or written to the cache. */
      noCacheTokens: number;
      /** Output tokens generated by the model. */
      outputTokens: number;
      /** Provider-specific usage metadata, e.g. `providerMetadata.anthropic`. */
      providerMetadata?: Record<string, unknown>;
    };

/**
 * Debug / observability configuration. Set on {@link AgentConfig.debug}
 * to get a lower-level view than `--verbose` / `--show-content`.
 *
 * Channels map 1:1 to {@link DebugEvent.channel} values. `all: true` is a
 * convenience that turns every channel on. Channel-specific flags take
 * precedence over `all` only in the sense that `all` is the baseline;
 * there's no way to "all-except" a channel intentionally — callers who
 * want that just list the channels they want explicitly.
 */
export interface DebugConfig {
  /** Log the resolved system prompt once per run. */
  systemPrompt?: boolean;
  /** Log the full message list before each model call. */
  messages?: boolean;
  /** Log request metadata (model, tool count, provider options). */
  request?: boolean;
  /**
   * Log raw stream parts returned by the provider. At `level: 'debug'` a
   * compact summary (partType only) is emitted; at `'trace'` the whole
   * part is attached. See `DebugConfig.traceResponseParts`.
   */
  response?: boolean;
  /** Log per-step cache read/write/no-cache token breakdown. */
  cache?: boolean;
  /** Turn every channel on. */
  all?: boolean;
  /**
   * When `response` is on, emit the full raw stream part object as
   * well as the partType. Off by default because stream parts can be
   * large; turn on when you specifically need to see them.
   */
  traceResponseParts?: boolean;
  /**
   * Cap the byte size of system-prompt / messages bodies in debug
   * events. Bodies exceeding this are truncated with a `truncated:
   * true` marker. Default: `16 * 1024` (16 KB).
   */
  maxBodyBytes?: number;
  /**
   * Where to send debug events. Default: they're also emitted as
   * `type: 'debug'` progress events so existing stream consumers can
   * see them. Provide a sink to route somewhere separate (e.g. a file
   * logger, stderr formatter, OpenTelemetry span emitter) — the sink
   * is called *in addition to* the progress event path.
   */
  sink?: (event: DebugEvent) => void | Promise<void>;
}

// Skill definition
export interface Skill {
  id: string;
  name: string;
  description: string;
  content: string; // The actual skill instructions
  author?: string;
  version?: string;
  /**
   * Verbatim user phrases that should trigger this skill. Concatenated into
   * the description when the manifest is built, so substring matching picks
   * up how users actually phrase requests (not just how authors name skills).
   * See issue #74 — addresses the "skills never fire because descriptions
   * don't match the user's voice" problem.
   */
  triggers?: string[];
}

/**
 * A search result returned by `SkillStore.search()`.
 *
 * Deliberately does **not** include a `url` field. Earlier drafts of the
 * interface carried `url?: string` and signalled "external skill registries
 * are fine to wire up." That's an SSRF / supply-chain footgun (see #34): a
 * hostile registry could return a URL the agent then auto-fetches with the
 * user's credentials. If you need URL-backed skill registries, build them
 * outside this interface, gate behind an explicit host allowlist, require
 * HTTPS, and never auto-install — `install()` must always receive the
 * verified content directly.
 */
export interface SkillSearchResult {
  id: string;
  name: string;
  description: string;
}

/**
 * Storage interface for skill definitions.
 *
 * Implementations are expected to be local (in-memory, OPFS, IndexedDB,
 * SQLite, …). External / network-backed implementations must not auto-fetch
 * skill content from `search()` results — see {@link SkillSearchResult}.
 */
export interface SkillStore {
  list(): Promise<Skill[]>;
  get(skillId: string): Promise<Skill | undefined>;
  install(skill: Skill): Promise<void>;
  remove(skillId: string): Promise<void>;
  search(query: string): Promise<SkillSearchResult[]>;
}

// ─── Routines (#77) ─────────────────────────────────────────────────────

/**
 * A routine input arg. Kept minimal — routines are prompts, not
 * typed DSLs. `optional` lets the routine author flag args that can be
 * omitted without failing; the model sees the metadata and can ask
 * the user for required args before calling `run_routine`.
 */
export interface RoutineInput {
  name: string;
  description?: string;
  optional?: boolean;
}

/**
 * A saved routine — a named procedure the agent can invoke by id.
 *
 * Routines differ from skills in *how they fire*: skills match against
 * task intent autonomously; routines are invoked explicitly by name
 * via `run_routine(id, args)`. They share the markdown-with-frontmatter
 * storage shape. See issue #77.
 */
export interface Routine {
  id: string;
  name: string;
  description: string;
  /** The routine body — markdown prompt, may contain `{{arg}}` placeholders. */
  body: string;
  inputs?: RoutineInput[];
  version?: string;
  /** Incremented every time the routine is invoked via `run_routine`. */
  runCount: number;
  /** ISO timestamp of the most recent invocation. Undefined for never-run. */
  lastRun?: string;
}

/**
 * Storage interface for routines.
 *
 * Implementations are expected to be local (in-memory, filesystem,
 * SQLite, …). Run metadata (`runCount`, `lastRun`) is tracked by the
 * store so a future agent-proposed capture pass can detect "I've done
 * this N times" without the agent having to maintain state.
 */
export interface RoutineStore {
  list(): Promise<Routine[]>;
  get(id: string): Promise<Routine | undefined>;
  save(routine: Routine): Promise<void>;
  remove(id: string): Promise<void>;
  /** Increment runCount and stamp lastRun. No-op if the routine doesn't exist. */
  recordRun(id: string): Promise<void>;
}

// ─── Scheduled tasks (#79) ──────────────────────────────────────────────

/**
 * A declarative scheduled task — a cron-driven agent run.
 *
 * Scheduled tasks turn agent-do into something closer to a long-running
 * colleague: inbox sweeps every 15 min, daily prep at 2am, weekly digest
 * on Sunday. Define them on {@link AgentConfig.scheduledTasks} and the
 * CLI / scheduler primitive will invoke them at the right time.
 *
 * The runtime does **not** itself daemonise — `scheduledTasks` is a
 * configuration primitive. Two execution surfaces use it:
 *
 * - `runScheduledTask(agent, task)` — fire a single task now, acquiring
 *   its lock file to prevent overlap with an already-running copy.
 * - `runScheduler(agent, tasks, options?)` — a foreground loop that
 *   ticks once per minute and fires any task whose cron expression
 *   matches.
 *
 * For production, most users will install a crontab entry per task
 * (via `agent-do scheduled-tasks install`) that calls
 * `agent-do scheduled-tasks run <id>` — the OS scheduler owns the
 * cadence, agent-do owns the lock file and logging.
 */
export interface ScheduledTask {
  /**
   * Unique task ID within this agent. Used as the lock-file name and
   * the handle for `agent-do scheduled-tasks run <id>`. Must match
   * `/^[a-zA-Z0-9_-]+$/` so it's safe as a filename.
   */
  id: string;
  /**
   * Standard 5-field cron expression (minute, hour, day-of-month,
   * month, day-of-week). Supports star, numeric, range, list, and
   * step forms — see `parseCron` in `src/scheduled-tasks.ts` for the
   * exact grammar. Numeric fields only; named months / weekdays are
   * not supported. Sunday is `0` (Saturday is `6`); `7` is also
   * accepted as Sunday for crontab compatibility.
   */
  cron: string;
  /**
   * Where the task's output goes:
   *
   * - `'isolated'` (default) — a fresh session. The task runs with no
   *   prior conversation history; good for stateless sweeps and
   *   digests.
   * - `'main'` — the task's output flows into the agent's main
   *   conversation. The caller is responsible for persisting and
   *   replaying that history (the scheduler just runs the turn); this
   *   is a primitive that higher-level hosts can build on.
   */
  sessionTarget?: 'isolated' | 'main';
  /**
   * How the payload is delivered to the agent:
   *
   * - `'agentTurn'` (default) — payload is injected as a user message,
   *   triggering a normal agent turn. Use for "please do X" prompts.
   * - `'systemEvent'` — payload is wrapped in a `<system-event>` envelope
   *   and the agent is invited to respond only when there's something
   *   to say (the "OK silence" contract). Use for heartbeat-style
   *   wakes.
   */
  wakeMode?: 'agentTurn' | 'systemEvent';
  /**
   * The payload delivered to the agent. Strings are used verbatim;
   * objects are serialised to JSON inside the envelope so the model
   * sees structured data.
   */
  payload: string | Record<string, unknown>;
  /**
   * Optional per-task description, shown in `scheduled-tasks status`
   * output.
   */
  description?: string;
}

/**
 * Persisted run record for a scheduled task. One entry per task ID,
 * updated on each run. Stored in `.agent-do/scheduler/status.json`.
 */
export interface ScheduledTaskStatus {
  id: string;
  /** ISO timestamp of the most recent run start. */
  lastRun?: string;
  /** ISO timestamp of the most recent run completion. */
  lastFinished?: string;
  /** 'success' | 'failed' | 'skipped' (lock held) | 'running'. */
  lastStatus?: 'success' | 'failed' | 'skipped' | 'running';
  /** Wall-clock duration of the most recent completed run. */
  durationMs?: number;
  /** Error message, set when `lastStatus === 'failed'`. */
  lastError?: string;
  /** Number of successful runs observed. */
  successCount?: number;
  /** Number of failed runs observed. */
  failureCount?: number;
}

