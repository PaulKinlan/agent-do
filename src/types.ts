// Agent configuration
export interface AgentConfig {
  id: string;
  name: string;
  model: LanguageModel;
  systemPrompt?: string; // Raw system prompt (CLAUDE.md content)
  tools?: ToolSet;
  /**
   * MCP (Model Context Protocol) servers to mount for this run (#75).
   *
   * Each entry spawns / connects to an MCP server; its tools are
   * namespaced as `mcp__<server>__<tool>` and merged into `tools`
   * before the loop starts. Permissions, hooks, usage tracking, and
   * `toolLimits` apply to MCP tools exactly as they do to local tools.
   *
   * The lifecycle is managed by the loop: servers are mounted once at
   * the start of each `run()` / `stream()` call and closed on
   * completion, abort, or error. See `src/mcp.ts` for transport
   * details (stdio, sse, streamable HTTP).
   */
  mcpServers?: McpServerConfig[];
  skills?: SkillStore;
  /**
   * Expose the privileged `install_skill` tool to the model (#24, H-05).
   *
   * Default **`false`**: the LLM cannot install skills, only search /
   * list / remove. Because installed skills get concatenated into the
   * system prompt on every subsequent run sharing the same store, a
   * prompt-injected agent with install access could rewrite its own
   * future instructions — a persistent jailbreak. Set `true` only when
   * the caller also runs a human-in-the-loop permission layer
   * (`permissions: { mode: 'ask' }` or `hooks.onPreToolUse`).
   */
  allowSkillInstall?: boolean;
  /**
   * Saved routines — named prompt-as-macro procedures (#77). When set,
   * the model gains `list_routines` and `run_routine(id, args)` tools
   * that surface and execute routines by name.
   */
  routines?: RoutineStore;
  /**
   * Expose the privileged `save_routine` tool to the model (#77).
   *
   * Default **`false`**: the LLM cannot save routines. Save is privileged
   * because a prompt-injected agent could persistently install a hostile
   * "routine" that future runs would execute when the user invokes it
   * by name. Same threat model as {@link allowSkillInstall}.
   */
  allowRoutineSave?: boolean;
  /**
   * How installed skills are injected into the system prompt (#74).
   *
   * - `'full'` — every skill's full body is dumped into the prompt (the
   *   pre-#74 behaviour). Good for small skill sets; works when the caller
   *   has counted the byte budget.
   * - `'manifest'` — only id / name / description (+ triggers) go in;
   *   bodies are fetched on demand via the `load_skill({ skillId })` tool.
   *   Essential for large skill libraries or cheaper models that can't
   *   absorb many skill bodies at once.
   * - `'auto'` (default) — start in `full`; switch to `manifest` once
   *   the combined skill bodies cross the `skillsManifestThreshold` byte
   *   budget. Gives the eager-load experience on tiny sets and the
   *   lazy-load experience on big ones without the caller choosing.
   */
  skillsMode?: 'full' | 'manifest' | 'auto';
  /**
   * Byte budget for the eager-load path in `skillsMode: 'auto'` (#74).
   * Default: `32 * 1024` (32 KB). Once the total of all skill bodies
   * exceeds this, auto-mode flips to `manifest`. Ignored for explicit
   * `skillsMode: 'full' | 'manifest'`.
   */
  skillsManifestThreshold?: number;
  maxIterations?: number;
  innerStepLimit?: number;
  hooks?: AgentHooks;
  permissions?: PermissionConfig;
  usage?: {
    enabled?: boolean;
    pricing?: PricingTable;
    limits?: {
      perRun?: number;
      perDay?: number;
    };
    /**
     * Hard cap multiplier for in-step cost checking (#31, M-07).
     *
     * `limits.perRun` is enforced in two layers with different timing:
     *
     * - **Soft limit (`perRun`)**: `UsageTracker.checkLimits()` runs
     *   at the top of each outer iteration. If an iteration's last
     *   step pushes cumulative cost over the soft limit, the loop
     *   breaks cleanly before the *next* iteration starts. One
     *   iteration's worth of spend may still be in flight when the
     *   soft check runs.
     *
     * - **Hard cap (`perRun × hardLimitMultiplier`)**: the per-step
     *   `onStepFinish` hook records each model step's usage and
     *   aborts the in-progress `streamText` call via AbortSignal as
     *   soon as cumulative cost crosses the hard cap. This bounds
     *   mid-iteration overshoot even in worst-case step chains.
     *
     * Default: `1.25` — gives the soft-limit check a comfortable
     * chance to fire between iterations before the hard cap does.
     */
    hardLimitMultiplier?: number;
    onLimitExceeded?: (event: {
      type: string;
      spent: number;
      limit: number;
    }) => Promise<boolean>;
  };
  /**
   * Per-run tool-call caps (#27, M-03).
   *
   * Without these, a runaway or prompt-injected agent can invoke thousands
   * of tool calls in a single outer iteration — `maxIterations` caps the
   * outer loop but `innerStepLimit` × unbounded-calls-per-step doesn't.
   *
   * - `maxToolCalls`: total cap across the entire run. Once exceeded,
   *   the wrapper returns a blocked `ToolResult` (with `reason:
   *   'tool-limit-run'`) rather than throwing; the model sees the
   *   error and can produce a final answer instead of crashing the
   *   loop.
   * - `maxToolCallsPerIteration`: resets at the start of each outer
   *   iteration. Same blocked-ToolResult semantics. Defends against
   *   tight per-iteration fan-out.
   *
   * Omit to disable (default). A sensible safe pair is `maxToolCalls: 100,
   * maxToolCallsPerIteration: 25`.
   */
  toolLimits?: {
    maxToolCalls?: number;
    maxToolCallsPerIteration?: number;
  };
  signal?: AbortSignal;
  /**
   * Include full raw tool results on `tool-result` progress events.
   *
   * Default (`false`): events carry only `summary` + structured `data`.
   * File contents, command output, and anything else the tool returned
   * stay out of the event stream. This keeps secrets out of CI logs and
   * telemetry by default.
   *
   * Set `true` when you need full fidelity for debugging or when you're
   * piping events to a trusted consumer that needs the raw payload.
   */
  emitFullResult?: boolean;
  /**
   * Number of recent outer iterations whose tool-output bodies are kept
   * verbatim in the conversation history. Older iterations get their
   * `<tool_output>...</tool_output>` bodies replaced with self-closing
   * markers, so injected content from a poisoned tool result can't
   * keep influencing the model on every subsequent step.
   *
   * Default `1` — only the most recent iteration's tool outputs flow in
   * full to the next call. The model still sees that the tool was
   * invoked (via the marker attributes) and the assistant's reasoning
   * about it, but the raw body is gone. See issue #33.
   *
   * Set to `Infinity` to keep the historical "everything stays in
   * context forever" behaviour.
   */
  historyKeepWindow?: number;
  /**
   * Declarative scheduled tasks (#79). Each entry binds a cron expression
   * to a payload that fires an agent run at its scheduled time.
   *
   * Validation happens once, at {@link createAgent} time: invalid cron
   * expressions, duplicate IDs, and unsafe task IDs throw synchronously
   * so misconfiguration fails loud. The runtime itself is optional —
   * declaring tasks does not automatically start a scheduler. Use
   * `runScheduler(agent, ...)` or install crontab entries via
   * `agent-do scheduled-tasks install` to actually drive them.
   */
  scheduledTasks?: ScheduledTask[];
  /**
   * Debug / observability hooks (#72). See {@link DebugConfig}.
   *
   * When set, the loop wraps `model` with a `wrapLanguageModel`
   * middleware that surfaces the resolved system prompt, the full
   * message list per step, per-step cache metrics, and (optionally)
   * every raw stream part. Events flow through the progress stream
   * as `type: 'debug'` and — if `debug.sink` is provided — to the
   * sink as well. No-op when `debug` is undefined, so there's zero
   * overhead for the default path.
   */
  debug?: DebugConfig;
}

// A message in conversation history
export interface ConversationMessage {
  role: 'user' | 'assistant';
  content: string;
}

// Filesystem store options
export interface FilesystemMemoryStoreOptions {
  /** Block all write operations. Agent can only read existing files. */
  readOnly?: boolean;
  /**
   * Called before any write/append/delete/mkdir operation.
   * Receives the canonicalized relative path (safe against ../ bypass).
   * Return true to allow, false to block.
   * Supports both sync and async callbacks.
   */
  onBeforeWrite?: (agentId: string, filePath: string, operation: 'write' | 'append' | 'delete' | 'mkdir') => boolean | Promise<boolean>;
}

// Agent instance
export interface Agent {
  readonly id: string;
  readonly name: string;
  run(task: string, context?: string, history?: ConversationMessage[]): Promise<string>;
  stream(task: string, context?: string, history?: ConversationMessage[]): AsyncIterable<ProgressEvent>;
  abort(): void;
}

// Run result
export interface RunResult {
  text: string;
  usage: RunUsage;
  steps: number;
  aborted: boolean;
}
